// Разработайте программу для управления списком игроков команды, используя стандартные методы класса List<T>.
// Требования:
//    Создайте список для хранения фамилий игроков (тип string).
//    Реализуйте следующие операции:
//        Добавление игроков "Иванов", "Петров", "Сидоров" методом Add.
//        Вставка игрока "Козлов" на позицию с индексом 1 методом Insert.
//        Проверка наличия игрока "Петров" в команде методом Contains.
//        Удаление игрока "Сидоров" методом Remove.
//        Поиск индекса игрока "Козлов" методом IndexOf.
//        Сортировка списка по алфавиту методом Sort.
//        Выведите текущий состав команды в формате:
//           1. Иванов  
//           2. Козлов  
//           3. Петров 
//        Проверьте, пуст ли список (свойство Count), и очистите его методом Clear.

// Пример вывода:
// Есть ли игрок 'Петров' в команде? True  
// Индекс игрока 'Козлов': 1  
// Текущий состав команды:  
//   1. Иванов  
//   2. Козлов  
//   3. Петров 
// Команда пуста? False 
// Количество игроков после очистки: 0 

namespace Task_01
{
    internal class Program
    {
        static void Main(string[] args)
        {
            List<string> players = new List<string>();

            // Добавление игроков
            players.Add("Иванов");
            players.Add("Петров");
            players.Add("Сидоров");

            // Вставка игрока
            players.Insert(1, "Козлов");

            // Проверка наличия игрока
            Console.WriteLine($"Есть ли игрок 'Петров' в команде? {players.Contains("Петров")}");

            // Удаление игрока
            players.Remove("Сидоров");

            // Поиск индекса
            Console.WriteLine($"Индекс игрока 'Козлов': {players.IndexOf("Козлов")}");

            // Сортировка
            players.Sort();

            // Вывод состава
            Console.WriteLine("Текущий состав команды:");
            for (int i = 0; i < players.Count; i++)
            {
                Console.WriteLine($"  {i + 1}. {players[i]}");
            }

            // Проверка на пустоту
            Console.WriteLine($"Команда пуста? {players.Count == 0}");

            // Очистка списка
            players.Clear();
            Console.WriteLine($"Количество игроков после очистки: {players.Count}");
        }
    }
}
